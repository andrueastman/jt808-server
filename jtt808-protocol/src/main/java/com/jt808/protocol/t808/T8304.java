package com.jt808.protocol.t808;

import io.github.yezhihao.protostar.annotation.Field;
import io.github.yezhihao.protostar.annotation.Message;
import com.jt808.protocol.basics.JTMessage;
import com.jt808.protocol.commons.JT808;

@Message(JT808.InformationService)
public class T8304 extends JTMessage {

    @Field(length = 1, desc = "type of information")
    private int type;
    @Field(lengthUnit = 2, desc = "textMessage")
    private String content;

    public T8304() {
    }

    public T8304(int type, String content) {
        this.type = type;
        this.content = content;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}